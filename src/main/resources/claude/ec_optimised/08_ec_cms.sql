-- =============================================================================
-- WEBSITE NAVIGATION MENU
-- FIX: index renamed idx_ec_menu_parent (collided with idx_menu_parent on app_menus)
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_menus
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    organization_id BIGINT NOT NULL
        REFERENCES org_organizations(id),

    parent_menu_id  BIGINT
        REFERENCES ec_menus(id),

    menu_name       VARCHAR(200),
    menu_url        VARCHAR(500),
    menu_icon       VARCHAR(100),
    target          VARCHAR(30) DEFAULT '_self',
    display_order   INTEGER DEFAULT 0,
    active          BOOLEAN DEFAULT TRUE
);

-- FIX: renamed to avoid collision with app_menus index
CREATE INDEX IF NOT EXISTS idx_ec_menu_parent ON ec_menus (parent_menu_id);
CREATE INDEX IF NOT EXISTS idx_ec_menu_org    ON ec_menus (organization_id);

-- =============================================================================
-- MEDIA LIBRARY  (file/image asset management)
-- Uses sec_users for uploaded_by (ERP staff upload product images)
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_media_library
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    organization_id BIGINT NOT NULL
        REFERENCES org_organizations(id),

    uploaded_by     BIGINT
        REFERENCES sec_users(id),

    file_name       VARCHAR(300),
    original_name   VARCHAR(300),
    file_url        VARCHAR(700),
    thumbnail_url   VARCHAR(700),
    mime_type       VARCHAR(100),
    file_size       BIGINT,
    image_width     INTEGER,
    image_height    INTEGER,
    alt_text        VARCHAR(300),
    uploaded_at     TIMESTAMP(6) DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_ec_media_org ON ec_media_library (organization_id);
