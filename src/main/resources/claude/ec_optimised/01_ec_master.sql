-- =============================================================================
-- SPINDLE ERP — ECOMMERCE MODULE  (OPTIMISED v3)
-- FILE 01 : MASTER — Categories, Product Catalog, Variants, Images
-- PostgreSQL 16+
--
-- OPTIMISATIONS FROM ENTITY ANALYSIS:
--
-- ec_product_catalog:
--   • item_id → inv_items (Item entity).  Item already owns:
--       itemCode, itemName, sku, barcode, costPrice, unitPrice, weight,
--       volume, dimensions, minimumStock, maximumStock, itemType,
--       hasLotTracking, brand, model, purchaseUnit, salesUnit
--     So ec_product_catalog holds ONLY the storefront/marketing layer.
--   • REMOVED: total_sales, total_views  (computed from ec_product_analytics)
--   • REMOVED: specification (use inv_items.internalNotes or a new text field
--     added to inv_items — no duplicate)
--   • minimum_order_qty / maximum_order_qty kept as NULLABLE override:
--     NULL = "use inv_items.minimumStock"; non-null = storefront override
--   • short_description retained (marketing blurb, not inv_items.description)
--
-- ec_product_variants:
--   • item_id FK → inv_items mandatory (each variant IS a separate Item)
--   • REMOVED: sku, barcode, cost_price  (read from Item via item_id)
--   • weight/length/width/height kept as NULLABLE physical overrides for
--     shipping-rate calculations (variant pack size differs from item master)
--   • selling_price / compare_price kept (storefront pricing layer)
--
-- ec_category_attributes / ec_product_attribute_values / ec_product_tags:
--   • These are STOREFRONT CMS constructs — no ERP equivalent → keep as-is
--
-- ItemBrand / ItemModel → already on inv_items.brand / inv_items.model
--   • No ec_brands table. Storefront brand is derived from item.brand.brandName
-- =============================================================================

-- =============================================================================
-- STOREFRONT CATEGORY  (CMS/SEO tree — separate from inv_item_categories)
-- inv_item_categories = inventory classification (stock type, costing)
-- ec_categories       = customer-facing navigation / PDP hierarchy
-- Linked implicitly via ec_product_catalog → inv_items → inv_item_categories
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_categories
(
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    -- BaseEntity pattern: org + audit columns
    created_at          TIMESTAMP(6),
    created_by          VARCHAR(100),
    updated_at          TIMESTAMP(6),
    updated_by          VARCHAR(100),
    organization_id     BIGINT NOT NULL REFERENCES org_organizations(id),

    parent_category_id  BIGINT REFERENCES ec_categories(id),

    category_code       VARCHAR(50)  NOT NULL,
    category_name       VARCHAR(200) NOT NULL,
    slug                VARCHAR(250) NOT NULL,

    short_description   VARCHAR(500),
    description         TEXT,

    image_url           VARCHAR(700),
    banner_url          VARCHAR(700),
    icon                VARCHAR(100),

    -- SEO (storefront-only, no ERP equivalent)
    meta_title          VARCHAR(255),
    meta_keywords       VARCHAR(500),
    meta_description    VARCHAR(1000),

    display_order       INTEGER  DEFAULT 0,
    level_no            INTEGER  DEFAULT 1,
    is_menu             BOOLEAN  DEFAULT TRUE,
    is_featured         BOOLEAN  DEFAULT FALSE,
    active              BOOLEAN  DEFAULT TRUE,
    deleted             BOOLEAN  DEFAULT FALSE,

    CONSTRAINT uq_ec_cat_code UNIQUE (organization_id, category_code),
    CONSTRAINT uq_ec_cat_slug UNIQUE (organization_id, slug)
);

CREATE INDEX IF NOT EXISTS idx_ec_cat_org      ON ec_categories (organization_id);
CREATE INDEX IF NOT EXISTS idx_ec_cat_parent   ON ec_categories (parent_category_id);
CREATE INDEX IF NOT EXISTS idx_ec_cat_slug     ON ec_categories (slug);
CREATE INDEX IF NOT EXISTS idx_ec_cat_active   ON ec_categories (active);
CREATE INDEX IF NOT EXISTS idx_ec_cat_featured ON ec_categories (is_featured);

-- =============================================================================
-- CATEGORY ATTRIBUTE DEFINITION  (filterable/searchable attribute schema)
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_category_attributes
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    created_at      TIMESTAMP(6),
    created_by      VARCHAR(100),
    updated_at      TIMESTAMP(6),
    updated_by      VARCHAR(100),

    category_id     BIGINT NOT NULL REFERENCES ec_categories(id) ON DELETE CASCADE,

    attribute_name  VARCHAR(150) NOT NULL,
    attribute_label VARCHAR(150),

    data_type       VARCHAR(30) NOT NULL
        CHECK (data_type IN ('TEXT','NUMBER','BOOLEAN','DATE','LIST','COLOR')),

    is_required     BOOLEAN DEFAULT FALSE,
    searchable      BOOLEAN DEFAULT TRUE,
    filterable      BOOLEAN DEFAULT TRUE,
    sortable        BOOLEAN DEFAULT FALSE,
    display_order   INTEGER DEFAULT 0,
    active          BOOLEAN DEFAULT TRUE
);

CREATE INDEX IF NOT EXISTS idx_ec_catattr_cat ON ec_category_attributes (category_id);

-- =============================================================================
-- PRODUCT CATALOG  (storefront/marketing layer on top of inv_items)
--
-- Relationship: ONE ec_product_catalog row per ONE inv_items row (1:1)
--
-- What this table adds over inv_items:
--   • Storefront slug / SEO meta
--   • Marketing title and rich descriptions
--   • Merchandising flags (featured, trending, new_arrival…)
--   • Publishing workflow (published, publish_date)
--   • CMS content (youtube_video, return_policy, shipping_information)
--   • Storefront category FK
--   • Order quantity overrides (NULL = use inv_items.minimumStock)
--
-- What this table does NOT duplicate from inv_items:
--   • itemCode, itemName, sku, barcode         → read via item_id
--   • costPrice, standardCost, unitPrice       → read via item_id
--   • weight, volume, dimensions               → read via item_id
--   • minimumStock, maximumStock               → use min/max_order_qty override
--   • brand, model                             → read via item_id.brand / item_id.model
--   • itemType, hasLotTracking                 → read via item_id
--   • purchaseUnit, salesUnit, operationUnit   → read via item_id
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_product_catalog
(
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    created_at              TIMESTAMP(6),
    created_by              VARCHAR(100),
    updated_at              TIMESTAMP(6),
    updated_by              VARCHAR(100),
    organization_id         BIGINT NOT NULL REFERENCES org_organizations(id),

    -- ── Core ERP link ──────────────────────────────────────────────────────
    -- MANDATORY: every product MUST be a published inv_items record
    -- (FINISHED_GOOD or SERVICE type recommended)
    item_id                 BIGINT NOT NULL REFERENCES inv_items(id),

    -- Storefront category (separate from inv_item_categories)
    category_id             BIGINT REFERENCES ec_categories(id),

    -- ── Storefront identity ────────────────────────────────────────────────
    slug                    VARCHAR(250) NOT NULL,
    product_title           VARCHAR(300) NOT NULL,  -- marketing name, may differ from itemName

    -- ── Rich content ──────────────────────────────────────────────────────
    short_description       VARCHAR(1000),
    description             TEXT,
    -- specification not duplicated here — use inv_items.internalNotes
    -- or request addition of a 'specification TEXT' column to inv_items

    -- ── SEO (storefront-only) ─────────────────────────────────────────────
    seo_title               VARCHAR(255),
    seo_keywords            VARCHAR(500),
    seo_description         VARCHAR(1000),

    -- ── CMS content ───────────────────────────────────────────────────────
    youtube_video           VARCHAR(500),
    warranty_information    VARCHAR(500),
    return_policy           TEXT,
    shipping_information    TEXT,

    -- ── Order quantity overrides ───────────────────────────────────────────
    -- NULL = inherit from inv_items.minimumStock / maximumStock
    -- Non-null = storefront-specific per-order limit (e.g. max 5 per customer)
    minimum_order_qty       NUMERIC(12,2),
    maximum_order_qty       NUMERIC(12,2),

    -- ── Merchandising flags ────────────────────────────────────────────────
    featured                BOOLEAN DEFAULT FALSE,
    best_seller             BOOLEAN DEFAULT FALSE,
    trending                BOOLEAN DEFAULT FALSE,
    new_arrival             BOOLEAN DEFAULT FALSE,
    recommended             BOOLEAN DEFAULT FALSE,

    -- ── Publishing ────────────────────────────────────────────────────────
    published               BOOLEAN DEFAULT FALSE,
    publish_date            TIMESTAMP(6),

    -- ── REMOVED: total_sales, total_views ─────────────────────────────────
    -- These are now computed from ec_product_analytics
    -- Service layer: SELECT SUM(purchases), SUM(product_views) WHERE product_id = ?
    -- No dual-write sync bug possible.

    active                  BOOLEAN DEFAULT TRUE,
    deleted                 BOOLEAN DEFAULT FALSE,

    CONSTRAINT uq_ec_prod_slug UNIQUE (organization_id, slug),
    CONSTRAINT uq_ec_prod_item UNIQUE (organization_id, item_id)  -- enforces 1:1
);

CREATE INDEX IF NOT EXISTS idx_ec_prod_org      ON ec_product_catalog (organization_id);
CREATE INDEX IF NOT EXISTS idx_ec_prod_item     ON ec_product_catalog (item_id);
CREATE INDEX IF NOT EXISTS idx_ec_prod_cat      ON ec_product_catalog (category_id);
CREATE INDEX IF NOT EXISTS idx_ec_prod_slug     ON ec_product_catalog (slug);
CREATE INDEX IF NOT EXISTS idx_ec_prod_featured ON ec_product_catalog (featured);
CREATE INDEX IF NOT EXISTS idx_ec_prod_best     ON ec_product_catalog (best_seller);
CREATE INDEX IF NOT EXISTS idx_ec_prod_trending ON ec_product_catalog (trending);
CREATE INDEX IF NOT EXISTS idx_ec_prod_pub      ON ec_product_catalog (published, publish_date);
CREATE INDEX IF NOT EXISTS idx_ec_prod_active   ON ec_product_catalog (active, deleted);

