-- =============================================================================
-- PRODUCT VARIANT
--
-- MANDATORY: item_id → inv_items (each variant IS a distinct inv_items record)
-- This is the key design decision: variants are not lightweight rows — they map
-- to actual inventory items with their own stock, lot tracking, and costing.
--
-- REMOVED (sourced from inv_items via item_id):
--   • sku     → inv_items.sku
--   • barcode → inv_items.barcode
--   • cost_price → inv_items.costPrice (service reads this for COGS)
--
-- KEPT as NULLABLE physical overrides (variant pack may differ from item master):
--   • weight, length, width, height — used by shipping-rate calculator
--     NULL = use inv_items.weight / inv_items.volume / inv_items.dimensions
--
-- KEPT as storefront pricing:
--   • selling_price (customer-facing — may differ from inv_items.unitPrice)
--   • compare_price (strikethrough price for discounts)
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_product_variants
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    created_at      TIMESTAMP(6),
    created_by      VARCHAR(100),
    updated_at      TIMESTAMP(6),
    updated_by      VARCHAR(100),

    product_id      BIGINT NOT NULL REFERENCES ec_product_catalog(id) ON DELETE CASCADE,

    -- MANDATORY: each variant maps to a distinct inv_items row for stock mgmt
    item_id         BIGINT NOT NULL REFERENCES inv_items(id),

    variant_code    VARCHAR(50),
    variant_name    VARCHAR(200),

    -- Visual differentiators (storefront-only)
    color           VARCHAR(100),
    size_name       VARCHAR(100),

    -- NULLABLE physical overrides (NULL = use item master values)
    -- Used only by shipping rate calculator; inventory uses item master weight
    weight_override NUMERIC(12,3),
    length_override NUMERIC(12,3),
    width_override  NUMERIC(12,3),
    height_override NUMERIC(12,3),

    -- Storefront pricing (may differ from inv_items.unitPrice)
    selling_price   NUMERIC(18,2),
    compare_price   NUMERIC(18,2),
    -- cost_price REMOVED — always read from inv_items.costPrice via item_id

    active          BOOLEAN DEFAULT TRUE,
    deleted         BOOLEAN DEFAULT FALSE,

    CONSTRAINT uq_ec_variant_item UNIQUE (product_id, item_id)  -- one item per variant slot
);

CREATE INDEX IF NOT EXISTS idx_ec_variant_prod ON ec_product_variants (product_id);
CREATE INDEX IF NOT EXISTS idx_ec_variant_item ON ec_product_variants (item_id);
CREATE INDEX IF NOT EXISTS idx_ec_variant_active ON ec_product_variants (active);

-- =============================================================================
-- PRODUCT ATTRIBUTE VALUES  (instance values for a specific product/variant)
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_product_attribute_values
(
    id                    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    product_id            BIGINT NOT NULL REFERENCES ec_product_catalog(id) ON DELETE CASCADE,
    category_attribute_id BIGINT NOT NULL REFERENCES ec_category_attributes(id) ON DELETE CASCADE,
    attribute_value       TEXT,

    CONSTRAINT uq_ec_prodattr UNIQUE (product_id, category_attribute_id)
);

CREATE INDEX IF NOT EXISTS idx_ec_prodattr_prod ON ec_product_attribute_values (product_id);

-- =============================================================================
-- PRODUCT TAGS
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_product_tags
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    organization_id BIGINT NOT NULL REFERENCES org_organizations(id),
    tag_name        VARCHAR(100) NOT NULL,
    slug            VARCHAR(120) NOT NULL,
    CONSTRAINT uq_ec_tag UNIQUE (organization_id, slug)
);

CREATE TABLE IF NOT EXISTS ec_product_tag_map
(
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id BIGINT NOT NULL REFERENCES ec_product_catalog(id) ON DELETE CASCADE,
    tag_id     BIGINT NOT NULL REFERENCES ec_product_tags(id)    ON DELETE CASCADE,
    CONSTRAINT uq_ec_prod_tag UNIQUE (product_id, tag_id)
);

CREATE INDEX IF NOT EXISTS idx_ec_prodtag_prod ON ec_product_tag_map (product_id);
