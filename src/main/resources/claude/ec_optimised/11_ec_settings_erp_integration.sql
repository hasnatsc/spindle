-- =============================================================================
-- ERP GL ACCOUNT DEFAULTS
--
-- Mirrors hrm_payroll_account_mappings pattern exactly:
--   • One row per organization
--   • All account IDs reference acc_chart_of_accounts (ChartOfAccount entity)
--   • Sub-account reference uses acc_chart_of_accounts_sub (ChartOfAccountSub)
--
-- EcErpBridgeService lookup priority:
--   1. Check ec_gl_account_defaults for org
--   2. Fall back to acc_mapping (existing ERP mapping if ECOMMERCE module_type exists)
--   3. Fall back to first matching account_type (ASSET/REVENUE/LIABILITY)
--
-- Aligns with JournalEntryLine.account (ChartOfAccount) and
-- JournalEntryLine.subAccount (ChartOfAccountSub)
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_gl_account_defaults
(
    id                              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    created_at                      TIMESTAMP(6) DEFAULT NOW(),
    updated_at                      TIMESTAMP(6),
    created_by                      VARCHAR(100),
    updated_by                      VARCHAR(100),

    organization_id                 BIGINT NOT NULL
        REFERENCES org_organizations(id),

    -- ── Revenue accounts (ChartOfAccount.accountType = REVENUE) ──────────────
    sales_revenue_account_id        BIGINT REFERENCES acc_chart_of_accounts(id),
    sales_returns_account_id        BIGINT REFERENCES acc_chart_of_accounts(id),

    -- ── Cost of Goods Sold (ChartOfAccount.accountType = EXPENSE) ────────────
    cogs_account_id                 BIGINT REFERENCES acc_chart_of_accounts(id),

    -- ── Accounts Receivable control (ChartOfAccount.accountType = ASSET) ─────
    -- Used when ec_customers.erp_sub_account_id IS NULL (no AR sub-account yet)
    accounts_receivable_id          BIGINT REFERENCES acc_chart_of_accounts(id),

    -- ── Tax (ChartOfAccount.accountType = LIABILITY) ──────────────────────────
    vat_payable_account_id          BIGINT REFERENCES acc_chart_of_accounts(id),

    -- ── Discount expense (ChartOfAccount.accountType = EXPENSE) ──────────────
    discount_expense_account_id     BIGINT REFERENCES acc_chart_of_accounts(id),

    -- ── Shipping income (ChartOfAccount.accountType = REVENUE) ───────────────
    shipping_income_account_id      BIGINT REFERENCES acc_chart_of_accounts(id),

    -- ── Default receiving bank/cash sub-account (ChartOfAccountSub STI) ──────
    -- BankAccount or CashAccount discriminator
    -- Overridden per-payment by ec_payments.receiving_sub_account_id
    default_bank_sub_account_id     BIGINT REFERENCES acc_chart_of_accounts_sub(id),

    CONSTRAINT uq_ec_gl_defaults UNIQUE (organization_id)
);

CREATE INDEX IF NOT EXISTS idx_ec_gl_defaults_org ON ec_gl_account_defaults (organization_id);

-- =============================================================================
-- ERP DOCUMENT MAPPING
-- Maps ecommerce lifecycle events to ERP DocumentType enum values
-- (matches global_business_documents.document_type CHECK constraint)
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_document_mapping
(
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    organization_id     BIGINT NOT NULL REFERENCES org_organizations(id),

    -- Ecommerce event name (internal EC lifecycle event)
    ec_document_type    VARCHAR(100),

    -- ERP DocumentType enum value (must match DocumentType enum exactly)
    -- e.g. 'SALES_INVOICE', 'DELIVERY_CHALLAN', 'CREDIT_NOTE', 'RECEIPT_VOUCHER'
    erp_document_type   VARCHAR(100),

    auto_create         BOOLEAN DEFAULT TRUE,

    -- Optional GL account overrides for this specific mapping
    debit_account_id    BIGINT REFERENCES acc_chart_of_accounts(id),
    credit_account_id   BIGINT REFERENCES acc_chart_of_accounts(id),

    CONSTRAINT uq_ec_docmap UNIQUE (organization_id, ec_document_type)
);

CREATE INDEX IF NOT EXISTS idx_ec_docmap_org ON ec_document_mapping (organization_id);

-- Seed canonical EC → ERP document type mappings
INSERT INTO ec_document_mapping (organization_id, ec_document_type, erp_document_type, auto_create)
SELECT
    o.id,
    e.ec_doc,
    e.erp_doc,
    TRUE
FROM org_organizations o
CROSS JOIN (VALUES
    ('ORDER_CONFIRMED',   'SALES_INVOICE'),
    ('PAYMENT_SUCCESS',   'RECEIPT_VOUCHER'),
    ('ORDER_SHIPPED',     'DELIVERY_CHALLAN'),
    ('RETURN_APPROVED',   'CREDIT_NOTE'),
    ('REFUND_ISSUED',     'PAYMENT_VOUCHER')
) AS e(ec_doc, erp_doc)
ON CONFLICT (organization_id, ec_document_type) DO NOTHING;

