-- =============================================================================
-- ECOMMERCE SETTINGS
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_settings
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    organization_id BIGINT NOT NULL REFERENCES org_organizations(id),
    setting_group   VARCHAR(100),
    setting_key     VARCHAR(150),
    setting_value   TEXT,
    data_type       VARCHAR(30)
        CHECK (data_type IN ('STRING','NUMBER','BOOLEAN','JSON')),
    description     TEXT,
    editable        BOOLEAN DEFAULT TRUE,
    created_at      TIMESTAMP(6) DEFAULT NOW(),
    updated_at      TIMESTAMP(6),
    CONSTRAINT uq_ec_setting UNIQUE (organization_id, setting_group, setting_key)
);

CREATE INDEX IF NOT EXISTS idx_ec_setting_group ON ec_settings (organization_id, setting_group);

