CREATE INDEX IF NOT EXISTS idx_ec_recent_prod ON ec_recently_viewed (product_id);
CREATE INDEX IF NOT EXISTS idx_ec_recent_time ON ec_recently_viewed (viewed_at DESC);

-- =============================================================================
-- CUSTOMER LOGIN HISTORY
-- =============================================================================

