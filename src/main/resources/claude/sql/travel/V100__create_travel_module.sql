create index idx_trv_hb_bsvc on trv_hotel_bookings (booking_service_id);
create index idx_trv_hb_hotel on trv_hotel_bookings (hotel_id);
create index idx_trv_hb_status on trv_hotel_bookings (status);
create index idx_trv_hb_dates on trv_hotel_bookings (check_in_date, check_out_date);

