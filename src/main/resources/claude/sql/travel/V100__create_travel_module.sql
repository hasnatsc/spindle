create table trv_air_tickets
(
    id                     bigint generated by default as identity primary key,
    created_at             timestamp(6),
    created_by             varchar(100),
    updated_at             timestamp(6),
    updated_by             varchar(100),
    pnr                    varchar(20),
    departure_date         date,
    departure_time         time(0),
    arrival_date           date,
    arrival_time           time(0),
    fare_amount            numeric(18, 2) not null default 0,
    tax_amount             numeric(18, 2) not null default 0,
    total_amount           numeric(18, 2) not null default 0,
    supplier_reference     varchar(100),
    status                 varchar(20)    not null default 'ISSUED'
        constraint trv_airticket_status_check
            check (status in ('ISSUED', 'CANCELLED', 'REFUNDED')),
    booking_service_id     bigint         not null
        constraint fk_trv_at_bsvc references trv_booking_services on delete cascade,
    airline_id             bigint
        constraint fk_trv_at_airline references trv_airlines,
    origin_airport_id      bigint
        constraint fk_trv_at_origin references trv_airports,
    destination_airport_id bigint
        constraint fk_trv_at_dest references trv_airports,
    cabin_class_id         bigint
        constraint fk_trv_at_cabin references trv_cabin_classes
);

create index idx_trv_at_bsvc on trv_air_tickets (booking_service_id);
create index idx_trv_at_status on trv_air_tickets (status);
create index idx_trv_at_departure on trv_air_tickets (departure_date);

