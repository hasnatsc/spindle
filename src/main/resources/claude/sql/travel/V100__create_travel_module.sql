create table trv_bookings
(
    id                  bigint generated by default as identity primary key,
    created_at          timestamp(6),
    created_by          varchar(100),
    updated_at          timestamp(6),
    updated_by          varchar(100),
    booking_no          varchar(50)    not null,
    booking_type        varchar(20)    not null
        constraint trv_bookings_type_check
            check (booking_type in ('PACKAGE', 'HOTEL', 'AIR', 'COMBINED')),
    booking_date        date           not null,
    travel_start_date   date,
    travel_end_date     date,
    status              varchar(20)    not null default 'DRAFT'
        constraint trv_bookings_status_check
            check (status in ('DRAFT', 'CONFIRMED', 'PARTIALLY_PAID', 'PAID', 'CANCELLED', 'COMPLETED')),
    currency            varchar(3)     not null default 'BDT',
    exchange_rate       numeric(18, 4) not null default 1,
    subtotal_amount     numeric(18, 2) not null default 0,
    discount_amount     numeric(18, 2) not null default 0,
    tax_amount          numeric(18, 2) not null default 0,
    total_amount        numeric(18, 2) not null default 0,
    paid_amount         numeric(18, 2) not null default 0,
    due_amount          numeric(18, 2) not null default 0,
    remarks             varchar(1000),
    organization_id     bigint         not null
        constraint fk_trv_booking_org references org_organizations,
    party_id            bigint
        constraint fk_trv_booking_party references acc_chart_of_accounts_sub,
    lead_id             bigint
        constraint fk_trv_booking_lead references crm_leads,
    opportunity_id      bigint
        constraint fk_trv_booking_opp references crm_opportunities,
    sales_agent_id      bigint
        constraint fk_trv_booking_agent references sec_users,
    approval_request_id bigint
        constraint fk_trv_booking_apr references apr_requests,
    journal_entry_id    bigint
        constraint fk_trv_booking_je references acc_journal_entry_master,
    constraint uq_trv_booking_org_no unique (organization_id, booking_no)
);

create index idx_trv_booking_org on trv_bookings (organization_id);
create index idx_trv_booking_status on trv_bookings (status);
create index idx_trv_booking_party on trv_bookings (party_id);
create index idx_trv_booking_date on trv_bookings (booking_date);
create index idx_trv_booking_agent on trv_bookings (sales_agent_id);

create table trv_booking_services
(
    id              bigint generated by default as identity primary key,
    created_at      timestamp(6),
    created_by      varchar(100),
    updated_at      timestamp(6),
    updated_by      varchar(100),
    service_type    varchar(20)    not null
        constraint trv_bookingsvc_type_check
            check (service_type in ('HOTEL', 'AIR', 'PACKAGE')),
    reference_id    bigint,
    description     varchar(500),
    quantity        numeric(12, 3) not null default 1,
    unit_cost       numeric(18, 2) not null default 0,
    unit_price      numeric(18, 2) not null default 0,
    discount_amount numeric(18, 2) not null default 0,
    tax_amount      numeric(18, 2) not null default 0,
    line_total      numeric(18, 2) not null default 0,
    booking_id      bigint         not null
        constraint fk_trv_bsvc_booking references trv_bookings on delete cascade,
    cost_center_id  bigint
        constraint fk_trv_bsvc_cc references org_cost_centers
);

create index idx_trv_bsvc_booking on trv_booking_services (booking_id);
create index idx_trv_bsvc_type on trv_booking_services (service_type);

create table trv_passengers
(
    id                bigint generated by default as identity primary key,
    created_at        timestamp(6),
    created_by        varchar(100),
    updated_at        timestamp(6),
    updated_by        varchar(100),
    title             varchar(10),
    first_name        varchar(100) not null,
    last_name         varchar(100),
    date_of_birth     date,
    gender            varchar(10)
        constraint trv_passenger_gender_check check (gender in ('MALE', 'FEMALE', 'OTHER')),
    passport_number   varchar(50),
    passport_expiry   date,
    nationality       varchar(100),
    passenger_type    varchar(10)  not null default 'ADULT'
        constraint trv_passenger_type_check check (passenger_type in ('ADULT', 'CHILD', 'INFANT')),
    is_lead_passenger boolean      not null default false,
    phone             varchar(30),
    email             varchar(150),
    remarks           varchar(500),
    booking_id        bigint       not null
        constraint fk_trv_pax_booking references trv_bookings on delete cascade
);

create index idx_trv_pax_booking on trv_passengers (booking_id);

create table trv_passenger_preferences
(
    id                  bigint generated by default as identity primary key,
    meal_preference     varchar(100),
    seat_preference     varchar(100),
    special_assistance  varchar(300),
