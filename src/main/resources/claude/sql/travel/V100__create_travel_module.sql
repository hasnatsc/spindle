    rooms_count         integer        not null default 1,
    adults              integer        not null default 1,
    children            integer        not null default 0,
    rate_per_night      numeric(18, 2),
    total_amount        numeric(18, 2) not null default 0,
    confirmation_number varchar(100),
    supplier_reference  varchar(100),
    status              varchar(20)    not null default 'PENDING'
        constraint trv_hotelbooking_status_check
            check (status in ('PENDING', 'CONFIRMED', 'CANCELLED', 'COMPLETED')),
    booking_service_id  bigint         not null
        constraint fk_trv_hb_bsvc references trv_booking_services on delete cascade,
    hotel_id            bigint         not null
        constraint fk_trv_hb_hotel references trv_hotels,
    room_type_id         bigint
        constraint fk_trv_hb_roomtype references trv_room_types,
    meal_plan_id        bigint
        constraint fk_trv_hb_mealplan references trv_meal_plans
