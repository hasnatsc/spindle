create table trv_hotel_categories
(
    id            bigint generated by default as identity primary key,
    created_at    timestamp(6),
    created_by    varchar(100),
    updated_at    timestamp(6),
    updated_by    varchar(100),
    category_name varchar(100) not null,
    description   varchar(500)
);

create table trv_hotels
(
    id               bigint generated by default as identity primary key,
    created_at       timestamp(6),
    created_by       varchar(100),
    updated_at       timestamp(6),
    updated_by       varchar(100),
    hotel_code       varchar(30)  not null,
    hotel_name       varchar(200) not null,
    city             varchar(100),
    country          varchar(100),
    address          varchar(500),
    star_rating      integer,
    contact_person   varchar(150),
    contact_phone    varchar(30),
    contact_email    varchar(150),
    is_active        boolean      not null default true,
    organization_id  bigint       not null
        constraint fk_trv_hotel_org references org_organizations,
    category_id      bigint
        constraint fk_trv_hotel_category references trv_hotel_categories,
    constraint uq_trv_hotel_org_code unique (organization_id, hotel_code)
);

create index idx_trv_hotel_org on trv_hotels (organization_id);
create index idx_trv_hotel_category on trv_hotels (category_id);
create index idx_trv_hotel_city on trv_hotels (city);

create table trv_room_types
