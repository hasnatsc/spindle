-- =============================================================================
-- AFFILIATE PROGRAM
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_affiliates
(
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    organization_id     BIGINT NOT NULL
        REFERENCES org_organizations(id),

    customer_id         BIGINT
        REFERENCES ec_customers(id),

    affiliate_code      VARCHAR(100),
    commission_rate     NUMERIC(8,2),

    total_sales         NUMERIC(18,2) DEFAULT 0,
    total_commission    NUMERIC(18,2) DEFAULT 0,
    payable_amount      NUMERIC(18,2) DEFAULT 0,

    status              VARCHAR(20) DEFAULT 'ACTIVE'
        CHECK (status IN ('ACTIVE','BLOCKED','SUSPENDED')),

    CONSTRAINT uq_ec_affiliate UNIQUE (organization_id, affiliate_code)
);

CREATE INDEX IF NOT EXISTS idx_ec_affiliate_cust ON ec_affiliates (customer_id);
CREATE INDEX IF NOT EXISTS idx_ec_affiliate_org  ON ec_affiliates (organization_id);

-- =============================================================================
-- REFERRAL TRACKING
