CREATE INDEX IF NOT EXISTS idx_ec_referral_aff ON ec_referrals (affiliate_id);

-- =============================================================================
-- PROMO CODES  (simpler than coupons — single-use disposable codes)
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_promo_codes
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    organization_id BIGINT NOT NULL
        REFERENCES org_organizations(id),

    promo_code      VARCHAR(50),
    description     TEXT,

    discount_type   VARCHAR(20)
        CHECK (discount_type IN ('PERCENTAGE','FIXED')),

    discount_value  NUMERIC(18,2),
    valid_from      TIMESTAMP(6),
    valid_to        TIMESTAMP(6),
    usage_limit     INTEGER,
    active          BOOLEAN DEFAULT TRUE,

    CONSTRAINT uq_ec_promo UNIQUE (organization_id, promo_code)
);

CREATE INDEX IF NOT EXISTS idx_ec_promo_org ON ec_promo_codes (organization_id);

-- =============================================================================
-- IN-APP CUSTOMER NOTIFICATIONS
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_notifications
(
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    organization_id     BIGINT NOT NULL
        REFERENCES org_organizations(id),

    customer_id         BIGINT
        REFERENCES ec_customers(id),

    notification_type   VARCHAR(30)
        CHECK (notification_type IN ('EMAIL','SMS','PUSH','IN_APP')),

    title               VARCHAR(300),
    message             TEXT,
    read_flag           BOOLEAN DEFAULT FALSE,
    sent_at             TIMESTAMP(6),
    created_at          TIMESTAMP(6) DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_ec_notif_cust ON ec_notifications (customer_id);
CREATE INDEX IF NOT EXISTS idx_ec_notif_org  ON ec_notifications (organization_id);

