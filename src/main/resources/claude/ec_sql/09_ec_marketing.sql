-- =============================================================================
-- NEWSLETTER SUBSCRIBERS
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_newsletter
(
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    created_at          TIMESTAMP(6) DEFAULT NOW(),

    organization_id     BIGINT NOT NULL
        REFERENCES org_organizations(id),

    email               VARCHAR(200) NOT NULL,
    full_name           VARCHAR(200),

    subscription_status VARCHAR(20) DEFAULT 'SUBSCRIBED'
        CHECK (subscription_status IN ('SUBSCRIBED','UNSUBSCRIBED','BOUNCED')),

    verification_token  VARCHAR(255),
    verified            BOOLEAN DEFAULT FALSE,
    subscribed_at       TIMESTAMP(6) DEFAULT NOW(),
    unsubscribed_at     TIMESTAMP(6),

    CONSTRAINT uq_ec_newsletter UNIQUE (organization_id, email)
);

CREATE INDEX IF NOT EXISTS idx_ec_newsletter_status ON ec_newsletter (subscription_status);
CREATE INDEX IF NOT EXISTS idx_ec_newsletter_org    ON ec_newsletter (organization_id);

-- =============================================================================
-- EMAIL MARKETING CAMPAIGNS
-- Distinct from ec_campaigns (price promotions).
-- These are email blast campaigns to newsletter subscribers.
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_email_campaigns
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    created_at      TIMESTAMP(6) DEFAULT NOW(),
    created_by      VARCHAR(100),
    updated_at      TIMESTAMP(6),

    organization_id BIGINT NOT NULL
        REFERENCES org_organizations(id),

    campaign_code   VARCHAR(50),
    campaign_name   VARCHAR(200),
    subject         VARCHAR(300),
    email_template  TEXT,

    scheduled_at    TIMESTAMP(6),
    sent_at         TIMESTAMP(6),
    total_sent      INTEGER DEFAULT 0,
    total_opened    INTEGER DEFAULT 0,
    total_clicked   INTEGER DEFAULT 0,

    campaign_status VARCHAR(20) DEFAULT 'DRAFT'
        CHECK (campaign_status IN
            ('DRAFT','SCHEDULED','RUNNING','COMPLETED','CANCELLED')),

    CONSTRAINT uq_ec_email_camp UNIQUE (organization_id, campaign_code)
);

CREATE INDEX IF NOT EXISTS idx_ec_emailcamp_status ON ec_email_campaigns (campaign_status);
CREATE INDEX IF NOT EXISTS idx_ec_emailcamp_org    ON ec_email_campaigns (organization_id);

-- =============================================================================
-- PUSH NOTIFICATIONS  (mobile app / PWA push)
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_push_notifications
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    created_at      TIMESTAMP(6) DEFAULT NOW(),
    created_by      VARCHAR(100),

    organization_id BIGINT NOT NULL
        REFERENCES org_organizations(id),

    title           VARCHAR(300),
    message         TEXT,
    image_url       VARCHAR(500),
    target_url      VARCHAR(500),

    send_to         VARCHAR(30)
        CHECK (send_to IN
            ('ALL','CUSTOMERS','VIP','NEW_CUSTOMERS','SEGMENT')),

    scheduled_at    TIMESTAMP(6),
    sent_at         TIMESTAMP(6),
    active          BOOLEAN DEFAULT TRUE
);

CREATE INDEX IF NOT EXISTS idx_ec_push_org ON ec_push_notifications (organization_id);

