-- =============================================================================
-- ECOMMERCE SETTINGS  (key-value store for store configuration)
-- Examples: store_name, currency_symbol, low_stock_threshold, timezone
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_settings
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    organization_id BIGINT NOT NULL
        REFERENCES org_organizations(id),

    setting_group   VARCHAR(100),   -- 'GENERAL' | 'PAYMENT' | 'SHIPPING' | 'SEO' | 'NOTIFICATION'
    setting_key     VARCHAR(150),
    setting_value   TEXT,

    data_type       VARCHAR(30)
        CHECK (data_type IN ('STRING','NUMBER','BOOLEAN','JSON')),

    description     TEXT,
    editable        BOOLEAN DEFAULT TRUE,
    created_at      TIMESTAMP(6) DEFAULT NOW(),
    updated_at      TIMESTAMP(6),

    CONSTRAINT uq_ec_setting UNIQUE (organization_id, setting_group, setting_key)
);

CREATE INDEX IF NOT EXISTS idx_ec_setting_group ON ec_settings (organization_id, setting_group);

-- =============================================================================
-- TAX CLASS  (e.g. 'STANDARD_VAT_15', 'ZERO_RATED', 'EXEMPT')
-- Referenced from ec_product_catalog via tax_class_id (add column if needed)
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_tax_classes
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    organization_id BIGINT NOT NULL
        REFERENCES org_organizations(id),

    class_code      VARCHAR(30),
    class_name      VARCHAR(150),
    description     TEXT,
    active          BOOLEAN DEFAULT TRUE,

    CONSTRAINT uq_ec_taxclass UNIQUE (organization_id, class_code)
);

-- =============================================================================
-- TAX RULES  (zone + class → rate matrix; Bangladesh has national 15% VAT)
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_tax_rules
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    organization_id BIGINT NOT NULL
        REFERENCES org_organizations(id),

    tax_class_id    BIGINT
        REFERENCES ec_tax_classes(id),

    country         VARCHAR(100) DEFAULT 'Bangladesh',
    division        VARCHAR(100),
    district        VARCHAR(100),

    tax_name        VARCHAR(100),
    tax_percent     NUMERIC(8,2),

    effective_from  DATE,
    effective_to    DATE,

    active          BOOLEAN DEFAULT TRUE
);

CREATE INDEX IF NOT EXISTS idx_ec_taxrule_class ON ec_tax_rules (tax_class_id);
CREATE INDEX IF NOT EXISTS idx_ec_taxrule_org   ON ec_tax_rules (organization_id);

