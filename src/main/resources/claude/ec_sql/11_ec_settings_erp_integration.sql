-- =============================================================================
-- ECOMMERCE GL ACCOUNT DEFAULTS
-- Per-organization default GL accounts for ecommerce postings.
-- These are looked up by EcErpBridgeService when no specific mapping override exists.
-- Pattern mirrors hrm_payroll_account_mappings.
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_gl_account_defaults
(
    id                          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    created_at                  TIMESTAMP(6) DEFAULT NOW(),
    updated_at                  TIMESTAMP(6),
    created_by                  VARCHAR(100),
    updated_by                  VARCHAR(100),

    organization_id             BIGINT NOT NULL
        REFERENCES org_organizations(id)
        CONSTRAINT uq_ec_gl_defaults UNIQUE,

    -- Revenue
    sales_revenue_account_id    BIGINT
        REFERENCES acc_chart_of_accounts(id),

    sales_returns_account_id    BIGINT
        REFERENCES acc_chart_of_accounts(id),

    -- COGS
    cogs_account_id             BIGINT
        REFERENCES acc_chart_of_accounts(id),

    -- Receivable (default AR — overridden by customer erp_sub_account_id)
    accounts_receivable_id      BIGINT
        REFERENCES acc_chart_of_accounts(id),

    -- Tax
    vat_payable_account_id      BIGINT
        REFERENCES acc_chart_of_accounts(id),

    -- Discount expense
    discount_expense_account_id BIGINT
        REFERENCES acc_chart_of_accounts(id),

    -- Shipping income
    shipping_income_account_id  BIGINT
        REFERENCES acc_chart_of_accounts(id),

    -- Default receiving bank/cash (overridden by ec_payments.receiving_sub_account_id)
    default_bank_sub_account_id BIGINT
        REFERENCES acc_chart_of_accounts_sub(id)
);

CREATE INDEX IF NOT EXISTS idx_ec_gl_defaults_org ON ec_gl_account_defaults (organization_id);

