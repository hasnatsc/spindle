-- =============================================================================
-- ERP DOCUMENT MAPPING  (maps ecommerce events to ERP document types)
-- Used by EcErpBridgeService to know which ERP doc type to create on each event
-- Examples:
--   ec_document_type='ORDER_CONFIRMED'  erp_document_type='SALES_INVOICE'
--   ec_document_type='PAYMENT_SUCCESS'  erp_document_type='RECEIPT_VOUCHER'
--   ec_document_type='ORDER_SHIPPED'    erp_document_type='DELIVERY_CHALLAN'
--   ec_document_type='RETURN_APPROVED'  erp_document_type='CREDIT_NOTE'
-- =============================================================================

CREATE TABLE IF NOT EXISTS ec_document_mapping
(
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    organization_id     BIGINT NOT NULL
        REFERENCES org_organizations(id),

    ec_document_type    VARCHAR(100),   -- renamed from ecommerce_document
    erp_document_type   VARCHAR(100),   -- renamed from erp_document (matches DocumentType enum)
    auto_create         BOOLEAN DEFAULT TRUE,

    -- Optional: GL accounts override for this mapping
    debit_account_id    BIGINT
        REFERENCES acc_chart_of_accounts(id),
    credit_account_id   BIGINT
        REFERENCES acc_chart_of_accounts(id),

    CONSTRAINT uq_ec_docmap UNIQUE (organization_id, ec_document_type)
);

CREATE INDEX IF NOT EXISTS idx_ec_docmap_org ON ec_document_mapping (organization_id);

-- Seed canonical document mapping (idempotent)
-- These tell EcErpBridgeService what ERP document to create for each ecommerce event
INSERT INTO ec_document_mapping (organization_id, ec_document_type, erp_document_type, auto_create)
SELECT
    o.id,
    event.ec_doc,
    event.erp_doc,
    TRUE
FROM org_organizations o
CROSS JOIN (VALUES
    ('ORDER_CONFIRMED',   'SALES_INVOICE'),
    ('PAYMENT_SUCCESS',   'RECEIPT_VOUCHER'),
    ('ORDER_SHIPPED',     'DELIVERY_CHALLAN'),
    ('RETURN_APPROVED',   'CREDIT_NOTE'),
    ('REFUND_ISSUED',     'PAYMENT_VOUCHER')
) AS event(ec_doc, erp_doc)
ON CONFLICT (organization_id, ec_document_type) DO NOTHING;

